public class ArithmeticOperation {
    public int getDivision(int value3, int value4) {
        return  (value3 / value4);
    }

    public  int getSum(int value1, int value2) {
        return  (value1 + value2);
    }

    public  int getSubtraction(int value3, int value4) {
        return (value3 - value4);
    }

    public  int getMultiplication(int value3, int value4) {
        return (value3 * value4);
    }
}
